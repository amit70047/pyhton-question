
a = 10
b = 5

# Addition
add = a + b

# Subtraction
subtract = a - b

# Multiplication
multiply = a * b

# Division 
if b == 0:
    divide = "Error: Cannot divide by zero"
else:
    divide = a / b

print("Addition:", add)
print("Subtraction:", subtract)
print("Multiplication:", multiply)
print("Division:", divide)
